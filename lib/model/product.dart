class Product {
  final int id;
  final String title;
  final String image;
  final String cat;
  final double price;
  final String description;
  final String point;

  Product({
    required this.id,
    required this.title,
    required this.image,
    required this.cat,
    required this.price,
    required this.description,
    required this.point,
  });
}
